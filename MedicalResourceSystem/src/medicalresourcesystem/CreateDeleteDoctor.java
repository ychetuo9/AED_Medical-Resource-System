    public void validateFields(){
        String name=txtName.getText();
        String email=txtEmail.getText();
        String mobileNumber=txtMobileNumber.getText();
        String specialty=txtSpecialty.getText();
        String hospital=(String)cbbHospital.getSelectedItem();
        String positionTitle=(String)cbbPositionTitle.getSelectedItem();
        String age=(String)cbbAge.getSelectedItem();
        String gender="";
        if(rbtnFemale.isSelected()){
            gender="Female";
        }
        if(rbtnMale.isSelected()){
            gender="Male";
        }
        
        if(!name.equals("")&&!age.equals("")&& !email.equals("")&& !mobileNumber.equals("")&& !specialty.equals("")&& !hospital.equals("")&& !positionTitle.equals("")&& !gender.equals(""))
            btnSave.setEnabled(true);
        else
            btnSave.setEnabled(false);
    }

