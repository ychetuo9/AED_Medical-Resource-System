    private void formComponentShown(java.awt.event.ComponentEvent evt) {//GEN-FIRST:event_formComponentShown
        // TODO add your handling code here:
        DefaultTableModel dtm = (DefaultTableModel)jTable1.getModel();
        ArrayList<Doctor> doctorList = DoctorDirectoryDao.getAllRecords();
        Iterator<Doctor> itrDocotr = doctorList.iterator();
        while(itrDocotr.hasNext()){
            Doctor doctorObj = itrDocotr.next();
            dtm.addRow(new Object[]{doctorObj.getId(),doctorObj.getName(),doctorObj.getEmail(),doctorObj.getGender(),doctorObj.getAge(),doctorObj.getMobileNumber(),doctorObj.getMobileNumber(),doctorObj.getHospital(),doctorObj.getPositionTitle(),doctorObj.getSpecialty()});
        }
        
        ArrayList<Hospital> hospitalList = HospitalDirectoryDao.getAllRecords();
        Iterator<Hospital> itrHospital = hospitalList.iterator();
        while(itrHospital.hasNext()){
            Hospital hospitalObj = itrHospital.next();
            cbbHospital.addItem(hospitalObj.getName());
        }
        
    }//GEN-LAST:event_formComponentShown

